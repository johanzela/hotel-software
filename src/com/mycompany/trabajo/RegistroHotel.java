    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombre = jTextField1.getText();
        String direccion = jTextField3.getText();
        String distrito = (String) jComboBox1.getSelectedItem();
        String telefono = jTextField2.getText();
        
        if (distrito.equals("<<Elegir un distrito>>")) {
            JOptionPane.showMessageDialog(this, "Ingrese un distrito válido para confirmar.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // Verificar si el número de teléfono contiene solo números
        if (!telefono.matches("^[0-9]+$")) {
            JOptionPane.showMessageDialog(this, "El número de teléfono solo puede contener números.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
    
        // Verificar si el número de teléfono tiene exactamente 9 dígitos
        if (telefono.length() != 9) {
            JOptionPane.showMessageDialog(this, "El número de teléfono debe tener exactamente 9 dígitos.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        Hotel hotel = new Hotel(nombre, direccion, distrito, telefono);
        HabitacionesHotel habitacionesHotel = new HabitacionesHotel(hotel);
        habitacionesHotel.setVisible(true);
        this.setVisible(false);
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

